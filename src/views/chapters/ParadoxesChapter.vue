    <h2> Twin Paradox </h2>

    <p>Suppose there are two identical twins, and one travels on a rocket
      far away into space near the speed of light. When that twin returns, he
      finds that his sibling has aged more due to the effects of time
      dilation. However, since both twins see each other as moving away from
      them, shouldn’t the effects of time dilation apply to both of them,
      leading to both of them finding that the other has aged more?</p>
    <p><img src="@/assets/images/021e43e78a7d7b5b9f0d609d6b548e0c018b9693.png" style="width:5.96875in;height:3.82292in"
