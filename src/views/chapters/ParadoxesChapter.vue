    <h2>Ladder paradox a.k.a Barn-Pole Paradox</h2>
    <p>Imagine if you will, a large pole say the length of 2u, and a barn
      with 2 doors, with the length of 1.6u, as in the figure below.</p>
    <p><img src="@/assets/images/7fae72385309a9fa9bb77b7010180e3a8e1bf399.png" style="width:5in;height:2.25in" /></p>
    <p>It is obvious that the pole could not possibly fit into the barn,
      right? Due to it simply being longer than the barn, the pole will stick
      out on one of the two ends.</p>
    <p>What if, now the pole was moving through the barn. At
      non-relativistic speeds, this would still hold true; as the pole travels
      through the barn, part of it would be visible throughout. Now, let’s
      think about what if the pole was moving through the barn at relativistic
      speeds. Length contraction now becomes a significant consideration. (As
      a refresher, length contraction is the phenomenon of where the length of
      a moving object as measured by one frame of reference is different than
      that measured in its own rest frame).</p>
    <p>As a result, from the inertial frame of the barn, the pole is moving
      at relativistic speeds and hence is therefore shorter and can thus fit
      into the barn comfortably.</p>
    <p><img src="@/assets/images/c8318f5db944e151b40c7726d46a962cb8a1cad7.png" style="width:5in;height:3.75in" /></p>
    <p>However, from the inertial frame of the pole, the barn is moving at
      relativistic speeds, and hence contracts, making it even less able to
      fit into the barn.</p>
    <p><img src="@/assets/images/cef8ade4125d1d52cc2bed26aeeef250274fd7b1.png" style="width:5in;height:3.75in" /></p>
    <p>How can it be that the ladder fits within the barn to one observer
      but not the other?</p>
    <p>This may seem like a cursory glance that there is a paradox, yet
      there is an easy explanation to all of this. It lies in the concept of
      the relativity of simultaneity. What <em>exactly</em> do we mean by “the
      pole fitting inside of the barn”? Let us define it as that the right
      side of the pole is to the left of the right door of the barn, what we
      will call condition 1 and the left side of the pole is to the right of
      the left door of the barn, what we will call condition 2, at the same
      time, I.e. simultaneously.</p>
    <p>This is partially also because time is relative to each frame of
      reference.</p>
    <p>In the reference frame of the barn, the two conditions occur at the
      same time, whereas in the reference frame of the pole, condition 1
      happens first in the absence of condition 2, then neither condition are
      fulfilled, afterwards condition 2 occurs in the absence of condition 1.
      (This can also be seen later using a Minkowski diagram, but we will have
      to wait for the video to see that later).</p>
    <h2>Terell Rotation</h2>
    <p>Imagine one is travelling at a significant speed with respect to
      light and observes a stationary cube.</p>
    <p>Now, one would expect that due to the Lorentz Transformations as
      elaborated above, there would be a contraction of the cube along the
      axis which one is travelling.</p>

    <img width="256" alt="Animated Terrell Rotation - Cube" src="@/assets/images/Animated_Terrell_Rotation_-_Cube.gif">

    <p>However, due to differences in the perception of the back of cubes
      and the front of the cubes, there is indeed an apparent rotation that
      one may notice whilst observing the cube.</p>
    <p>Let us try to explain this phenomenon. The following image is of the
      motion of the observer and of the cube from above.</p>
    <p><img src="@/assets/images/216307f38f3200c68c404dfd29388528d809749b.gif"
        style="width:3.11458in;height:2.60417in" /></p>
    <p>Now, one would expect that the light from the back of the cube, in
      this case D and C would be emitted first, than the light from the front
      of the cube, at A and B. Specifically, it would have to have travelled a
      further distance <em>l,</em> the length of a side of the cube, than the
      light coming from A or B.</p>
    <p><img src="@/assets/images/5e37e082fc14f0828f385417e733df98087c2f9d.gif"
        style="width:3.20833in;height:2.86458in" /></p>
    <p>Thus, when applying relativity, the observer would have
      simultaneously seen the earlier image of the back side of the cube, D’,
      and a later image of the front side of the cube A. </p>
      <p> The back face of the cube, would then have appeared to have shifted against the direction that the observer is traveling in.
      This causes it to appear to have rotated horizontally.</p>
    <p>We must remember that according to Einstein’s first postulate that
      the laws of physics must be identical in all inertial frames, hence this
      Terrell rotation must be a purely visual effect – and has no real world
      implications on the state of the cube itself.</p>
    <p>This is the famous rotation that Terell and Penrose had both
      independently proposed in 1959. An interesting fact of this is that it
      taken a long time for physicists to realise that such an effect indeed
      took place, even 45 years Einstein had proposed Special Relativity.</p>
    <p>This shows us that we should always try to fact-check our
      presumptions in certain contexts, as we may always find new and
      interesting phenomena just lurking beneath physical theories.</p>
